<p><span class="imgLeft"><img style="float: left;" src="../resources/img/news/FacultyLectureSeriesSp19.jpg" alt="Faculty Lecture Series" width="235" height="156"></span>FEBRUARY 6, 2019--Allan Hancock College is hosting a free lecture on Friday, Feb.
   8 to help anyone interested in writing and publishing a book. The lecture, titled
   “So You Want to be an Author,” will be given by Hancock speech professor and author
   Diane Auten from 6-8 p.m. in the forum (Room C-40) on the Santa Maria campus.
</p>

<p>“Publishing a book was always a goal of mine, but I was so lost in the process,” said
   Auten. “In the lecture we’re going to discuss the steps you need to take in order
   to get your book out and into the world - everything from overcoming the fear of telling
   your story, to trying to get your book traditionally published or self-published.”
</p>

<p>Auten will draw on her own experiences of recently publishing an award-nominated book,
   “The Joy of Raising Boys: The Good, the Bad, and the Hilarious,” an amusing “momoir”
   about the trials and tribulations of raising boys.
</p>

<p>“After many years of sharing funny stories and quotes from my sons on social media,
   people would always say I should write a book. So, I did,” said Auten. “If I can do
   it, others can do it as well, and I hope my lecture can inspire others to get out
   there and get published!”
</p>

<p>“So You Want to be an Author” is open to the public and will be the first lecture
   is a series of Friday night films and lectures sponsored by the college’s Community
   Education program during the spring semester. There is no fee to attend the lecture,
   but registration is required. Registration assistance is available in-person in the
   Community Education building (bldg. S) on Hancock’s Santa Maria campus. To register
   online:
</p>

<ol>
   
   <li>Enroll at Hancock by going to&nbsp;<a href="http://www.hancockcollege.edu/apply/">www.hancockcollege.edu/apply/</a> and completing the CCCApply application.
   </li>
   
   <li>Register for the course by going to myHancock from the Allan Hancock College website
      (<a href="http://www.hancockcollege.edu">www.hancockcollege.edu</a>), and click on Admissions &amp; Registration, then “Register for Classes.” Register for
      OLDR 7102A Current Topics (CRN 42137).
   </li>
   
</ol>

<p>Registration will not be available onsite the day of the lecture. However, upon registering
   for a lecture series course, registrants can attend any of the other lectures or film
   screenings taking place during the spring semester without having to register again.
</p>

<p>Other topics in the series offered this spring semester will include: “Sherlock Holmes,
   Consulting Detective and Fictional Superstar” on Feb. 22; a three-part series of German
   films on March 1, 8 and 15; “American Horror, Fiction and Film” on April 5; “Exploring
   Mindfulness in Today’s World” on March 29 and “Walter Benjamin’s ‘The Storyteller’
   – Where Does America Go Next?” on April 19.&nbsp; More information on these topics can
   be found at&nbsp;<a href="http://www.hancockcollege.edu/lectures/">www.hancockcollege.edu/lectures</a>. All lectures and film screenings will take place 6-8 p.m. in Room C-40 on the Santa
   Maria campus.
</p>

